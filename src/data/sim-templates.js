/**
 * 카테고리의 titles/bodies/comments/replies를 2배로 확장하고(중복 제거),
 * quiz.questions를 5배로 확장한다.
 */
function ensureExpanded(category, catObj){
  if (_expandedCache.has(category)) return _expandedCache.get(category);

  const clone = JSON.parse(JSON.stringify(catObj));
  if (clone.titles)   clone.titles   = expand2xUnique(clone.titles);
  if (clone.bodies)   clone.bodies   = expand2xUnique(clone.bodies);
  if (clone.comments) clone.comments = expand2xUnique(clone.comments);
  if (clone.replies)  clone.replies  = expand2xUnique(clone.replies);

  if (category === "quiz" && Array.isArray(clone.questions)) {
    clone.questions = expandQuizTo5x(clone.questions, 5);
  }

  _expandedCache.set(category, clone);
  return clone;
}

