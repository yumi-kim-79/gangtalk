/**
 * 활동 트리거에 따른 자연 증가 시뮬레이션
 * @param metrics {{views:number, likes:number, comments:number}}
 * @param activity {'view'|'like'|'comment'|'boost'}
 * @returns 동일 객체(변이)
 */
export function bumpMetrics(metrics, activity='view'){
  if (!metrics) return metrics;
  const inc = (k, v)=> metrics[k] = Math.max(0, Math.floor(metrics[k] + v));
  switch(activity){
    case 'view':    inc('views', randInt(1,5)); break;
    case 'like':    inc('likes', 1); inc('views', randInt(1,3)); break;
    case 'comment': inc('comments', 1); inc('views', randInt(2,6)); break;
    case 'boost':   inc('views', randInt(15,60)); inc('likes', randInt(2,8)); break;
  }
  return metrics;
}

