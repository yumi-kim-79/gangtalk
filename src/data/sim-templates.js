/*********************************
 * ✅✅ 추가: 실운영 글의 조회수/추천수 자동 랜덤 상승 타이머
 * - 힐링톡/야호톡 등 실제 문서에 주기적으로 자연 증가치를 반영
 * - 화면 진입 시 start, 화면 나갈 때 stop 해주세요.
 *********************************/

// 내부 타이머 저장소
const _metricTimers = new Map();

/**
 * 내부: 한 번의 틱에서 증가량을 뽑는다.
 * @param {'view'|'like'|'comment'|'boost'} activity
 * @returns {{views:number, likes:number, comments:number}}
 */
function _deltaByActivity(activity) {
  switch (activity) {
    case 'like':
      return { views: randInt(1, 3), likes: 1, comments: 0 };
    case 'comment':
      return { views: randInt(2, 6), likes: 0, comments: 1 };
    case 'boost': // 노출 증가 상황 가정
      return { views: randInt(15, 60), likes: randInt(2, 8), comments: 0 };
    case 'view':
    default:
      return { views: randInt(1, 5), likes: 0, comments: 0 };
  }
}

/**
 * 내부: 확률에 따라 액티비티 선택
 */
function _pickActivityByProb(p = { view: 0.76, like: 0.12, comment: 0.08, boost: 0.04 }) {
  const sum = Math.max(0.0001, (p.view ?? 0) + (p.like ?? 0) + (p.comment ?? 0) + (p.boost ?? 0));
  const pv = (p.view ?? 0) / sum, pl = (p.like ?? 0) / sum, pc = (p.comment ?? 0) / sum, pb = (p.boost ?? 0) / sum;
  const r = Math.random();
  if (r < pb) return 'boost';
  if (r < pb + pc) return 'comment';
  if (r < pb + pc + pl) return 'like';
  return 'view';
}

/**
 * 글 상세/목록 노출 동안 자동으로 메트릭을 올려준다.
 * Firestore 문서에 increment로 누적 반영.
 *
 * @param {string} collectionPath 예: 'posts' 또는 'boards/healing/posts'
 * @param {string} docId          글 문서 ID
 * @param {object} options
 *  - minMs: 최소 틱 간격(기본 35초)
 *  - maxMs: 최대 틱 간격(기본 120초)
 *  - probs: {view, like, comment, boost} 확률 합=1 권장
 *  - guardSim: authorUid === "__SIM__" 등 시뮬 문서는 막기 (기본 true)
 */
export function startAutoMetricsTickerForDoc(
  collectionPath,
  docId,
  options = {}
) {
  const key = `${collectionPath}/${docId}`;
  if (_metricTimers.has(key)) return; // 중복 방지

  const {
    minMs = 12_000,
    maxMs = 28_000,
    probs = { view: 0.55, like: 0.12, comment: 0.08, boost: 0.04 },
    guardSim = true,
  } = options;

  // 즉시 1회 조회 증가(상세 진입 효과)
  recordUserActivity('view', collectionPath, docId);

  let cancelled = false;

  async function tickOnceAndSchedule() {
    if (cancelled) return;

    try {
      // 선택 확률로 액티비티 결정
      const activity = _pickActivityByProb(probs);
      const d = _deltaByActivity(activity);

      // 필드 매핑(기본: 상위 필드 사용)
      const fields = options.fields ?? { views: 'views', likes: 'likes', comments: 'comments' };

      const ref = fsDoc(db, collectionPath, docId);
      await updateDoc(ref, {
        'metrics.views': increment(d.views),
        'metrics.likes': increment(d.likes),
        'metrics.comments': increment(d.comments),
      });
    } catch (err) {
      // 네트워크/권한 오류는 조용히 로그만 남김
      console.warn('[metrics-ticker] update skipped:', {
        code: err?.code, name: err?.name, message: err?.message, stack: err?.stack
      });
    }

    // 다음 틱 예약(랜덤 지터)
    const nextDelay = randInt(minMs, maxMs);
    const t = setTimeout(tickOnceAndSchedule, nextDelay);
    _metricTimers.set(key, t);
  }

  // 첫 예약
  const firstDelay = randInt(Math.floor(minMs / 2), minMs);
  const t = setTimeout(tickOnceAndSchedule, firstDelay);
  _metricTimers.set(key, t);

  // stop 함수 반환(선택 사용)
  return () => stopAutoMetricsTicker(docId, collectionPath);
}

/**
 * 타이머 중단
 */
export function stopAutoMetricsTicker(docId, collectionPath = 'posts') {
  const key = `${collectionPath}/${docId}`;
  const t = _metricTimers.get(key);
  if (t) {
    clearTimeout(t);
    _metricTimers.delete(key);
  }
}

/**
 * 사용자의 실제 액션을 기록하여 자연스러운 증가를 즉시 반영.
 * - 상세 진입: recordUserActivity('view', 'posts', postId)
 * - 좋아요:     recordUserActivity('like', 'posts', postId)
 * - 댓글 작성:  recordUserActivity('comment', 'posts', postId)
 */

export async function recordUserActivity(
  activity = 'view',
  collectionPath = 'posts',
  docId,
  options = {}
) {

  try {
    const d = _deltaByActivity(activity);
    // 호출 쪽에서 동일 옵션을 넘기지 않아도 기본값으로 상위 필드 반영
    // 함수 시그니처가 recordUserActivity(activity='view', collectionPath='posts', docId, options={}) 라면:
    const fields = (options && options.fields) ?? { views: 'views', likes: 'likes', comments: 'cmtCount' };

    const ref = fsDoc(db, collectionPath, docId);
    await updateDoc(ref, {
      [fields.views]:    increment(d.views),
      [fields.likes]:    increment(d.likes),
      [fields.comments]: increment(d.comments),
    });

  } catch (e) {
    console.warn('[metrics-activity] update skipped:', e?.message || e);
  }
}

/**
 * (선택) 일괄 시작 헬퍼 — 목록에서 여러 글을 동시에 노출할 때
 * @param {Array<{id:string}>} items 글 배열(각 item.id 필요)
 * @param {string} collectionPath
 */
export function startAutoMetricsTickerForList(items = [], collectionPath = 'posts', opts = {}) {
  (items || []).forEach(it => {
    if (!it?.id) return;
    startAutoMetricsTickerForDoc(collectionPath, it.id, {
      minMs: 15_000, maxMs: 35_000,
      probs: { view: 0.65, like: 0.18, comment: 0.12, boost: 0.05 },
      ...opts
    });
  });
}

