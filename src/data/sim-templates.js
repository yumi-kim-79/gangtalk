export function pick(arr){ return arr[Math.floor(Math.random() * arr.length)] }
export function randInt(min, max){ return Math.floor(Math.random() * (max - min + 1)) + min }

// 최근 N개 히스토리(문자열)와 겹치면 재추첨
export function uniquePick(arr, recentSet, maxTry = 30) {
  if (!recentSet) return pick(arr);
  let val = null;
  for (let i = 0; i < maxTry; i++) {
    const cand = pick(arr);
    if (!recentSet.has(cand)) { val = cand; break; }
  }
  return val ?? pick(arr);
}

// 제목/본문/댓글/대댓글 각각의 최근 히스토리 저장용(선택)
export const recentSets = {
  title: new Set(), body: new Set(), comment: new Set(), reply: new Set()
};
export function rememberRecent(kind, text, keep = 400) {
  const s = recentSets[kind]; if (!s) return;
  s.add(text);
  if (s.size > keep) {
    const arr = Array.from(s);
    s.clear();
    for (let i = arr.length - keep; i < arr.length; i++) if (i >= 0) s.add(arr[i]);
  }
}

/*********************************
 * 글/댓글/대댓글 — 2배 자동 확장 변주기
 *********************************/
const VAR_PREFIX = ["[오늘]", "[현실]", "[TMI]", "[메모]", "[소소]", "[주의]", "(*기록*)", "", ""];
const VAR_SUFFIX = [
  " (기록용)", " — 끝!", " ✅", " ✍️", " 😊", " 😂", " 🙌", " 💡", " 🔥", " ☕️", ""
];
const VAR_EMOJIS = ["", "✨", "🌿", "🌤️", "📝", "🎧", "📚", "🏃", "🍀", "🧘", "📷", ""];

function dedupe(arr){
  const seen = new Set(); const out = [];
  for (const s of arr) {
    const k = (s||"").trim();
    if (!k) continue;
    const norm = k.toLowerCase();
    if (!seen.has(norm)) { seen.add(norm); out.push(k); }
  }
  return out;
}

function expand2xUnique(list){
  const base = dedupe(list);
  const extra = [];
  for (const s of base) {
    // 의미 훼손 없이 토닝/이모지/접두어·접미어 변주
    const p = pick(VAR_PREFIX);
    const e = pick(VAR_EMOJIS);
    const t1 = `${p ? p + " " : ""}${s}${pick(VAR_SUFFIX)}`;
    const t2 = `${s}${e ? " " + e : ""}`;
    extra.push(t1, t2);
  }
  const merged = dedupe([...base, ...extra]);
  // 최소 2배 충족
  if (merged.length < base.length * 2) {
    while (merged.length < base.length * 2) {
      merged.push(`${pick(VAR_PREFIX)} ${pick(base)} ${pick(VAR_SUFFIX)}`.trim());
    }
  }
  return merged;
}

