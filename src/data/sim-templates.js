export function expandQuizTo5x(baseQuestions, targetMultiple=5){
  const base = baseQuestions.slice();
  const need = base.length * targetMultiple;
  let pool = [
    ...genMathMCQ(28),
    ...genPrimeMCQ(14),
    ...genOXFacts(40),
    ...genNonsenseText(40),
    ...genUnitConvertText(18),
    ...genFlagsMCQ(20)
  ];
  // 중복 제거(질문 텍스트 기준)
  const seenQ = new Set(base.map(q=>normalize(q.q)));
  const out = base.slice();
  for (const q of pool) {
    const key = normalize(q.q);
    if (!seenQ.has(key)) { seenQ.add(key); out.push(q); }
    if (out.length >= need) break;
  }
  // 부족하면 임의 수학퀴즈로 채움
  while (out.length < need) {
    const extra = genMathMCQ(1)[0];
    const key = normalize(extra.q);
    if (!seenQ.has(key)) { seenQ.add(key); out.push(extra); }
  }
  return out;
}

/*********************************
 * 퀴즈 정답 검증 로직 (공개 API)
 *********************************/
export function isQuizCorrect(text, question) {
  if (!question) return { correct: false, matched: "" };
  const t = normalize(text);
  const ans = normalize(question.answer);
  if (t === ans) return { correct: true, matched: question.answer };
  for (const a of (question.accept || [])) {
    if (normalize(a) === t) return { correct: true, matched: a };
  }
  if (question.type === "ox") {
    if (["o","x"].includes(t) && normalize(question.answer) === t) {
      return { correct: true, matched: t.toUpperCase() };
    }
  }
  if (question.type === "mcq") {
    const num = t.match(/\d+/)?.[0];
    if (num && normalize(num) === normalize(question.answer)) {
      return { correct: true, matched: num };
    }
    // 보기 텍스트 직접 매칭 허용
    if (question.accept?.some(a => normalize(a) === t)) {
      return { correct: true, matched: text };
    }
  }
  return { correct: false, matched: "" };
}

